    for i in range(0,len(x)):
        pos1=amino.index(x[i])#find the place of amino acid presented in x[i] in amino[]
        pos2=amino.index(y[i])
        track1=blosum[int(pos1)][int(pos2)]#find the score in blosum[]
        score+=track1#calculate the alignment score between two sequence
        if pos1==pos2:
            percent+=1
        percent=percent/len(x)*100
    print('Sequence1 name:',str(a))#print the seq1 name
    print('Sequence1:',x)#print seq1
    print('Sequence2 name:',str(b))
    print('Sequence2:',y)
    print('Alignment score:',score)#print score
    print('The percentage of identical amino acids:',percent,'%')#print the percentage of identical amino acid
